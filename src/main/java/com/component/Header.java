    @Override //* Dibuja|Renderiza el componente
    protected void paintComponent(Graphics g) {
        super.paintComponent(g);
        Graphics2D g2d = (Graphics2D) g;

        // Antialiasing para mejor calidad
        g2d.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
        
        // Dibujar l√≠neas blancas decorativas
        g2d.setColor(Color.WHITE);
        g2d.setStroke(new BasicStroke(2));
        
        g2d.drawLine(130, 30, 147, 48);
        g2d.drawLine(130, 40, 150, 60);
        
        g2d.drawLine(147, 48, 163, 0);
        g2d.drawLine(150, 60, 166, 13);
        
        g2d.drawLine(169, 0, 184, 48);
        g2d.drawLine(166, 13, 180, 60);
        
        g2d.drawLine(184, 48, 201, 34);
        g2d.drawLine(180, 60, 200, 41);
        
        g2d.drawLine(202, 34, 250, 34);
        g2d.drawLine(201, 40, 255, 40);
        
        g2d.drawArc(250, 20, 60, 30, 180, -180);
        g2d.drawArc(255, 25, 50, 30, 180, -180);
        
        g2d.drawLine(310, 34, getWidth()-30, 34);
        g2d.drawLine(305, 40, getWidth()-30, 40);
        
        g2d.drawLine(getWidth()-30, 34, getWidth()-30, 40);
        // Dibujar una curva del footer
        widthArc = getWidth();
        xArc = -widthArc/2;
        yArc = getHeight() - heightArc/2;
        
        g2d.fillArc(xArc, yArc, widthArc, heightArc, 0, arcAngle);
//        g2d.fillArc(widthArc/2, -heightArc/2, widthArc, 40, -arcAngle, -arcAngle);
        
        g2d.drawArc(xArc, yArc - 10 , widthArc + 8, heightArc, 0, arcAngle);
        
        g2d.drawArc(-xArc + 9, yArc + 18, 20, 6, 180, 90);
        
        int x = -xArc + 9 + 10, y = yArc + 24; 
        
        g2d.drawLine(x, y, getWidth(), y);
    }


